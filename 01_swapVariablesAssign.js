console.log("========== Before Swap ==========");

var sweety; // Variable Declaration
sweety="Sweety";
var cutie;
cutie="Cutie";
console.log("sweety:",sweety,"cutie:",cutie);

console.log("========== After Swap ==========");
var temp=sweety;
sweety=cutie;
cutie=temp;
console.log("sweety:",sweety,"cutie:",cutie);

